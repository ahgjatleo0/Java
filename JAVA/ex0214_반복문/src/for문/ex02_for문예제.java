package for문;


public class ex02_for문예제 {

	public static void main(String[] args) {

		int i = 0;
		
		for (i = 96; i > 72; i--) {
			
			System.out.print(i + " ");
			
		}
		System.out.println();	

		for (i = 96; i > 72; i--) {


			if (i % 2 != 0) {

				System.out.print(i + " ");

			}


		}

	}

}
